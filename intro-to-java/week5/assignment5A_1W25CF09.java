public class assignment5A_1W25CF09 {
  public static void main(String args[]) {
    for (int i = 20; i >= 0; i--) {
      System.out.println("for: " + (i * 5));
    }
    System.out.println("Finish"); 

    int j = 20; 
    while (j >= 0){
        System.out.println("while: " + (j * 5)); 
        j --; 
    }
    System.out.println("Finish"); 
  }
}
